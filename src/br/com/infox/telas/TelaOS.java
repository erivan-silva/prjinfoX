    private void excluir_os() {
        int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir esta OS?", "ATENÇÃO", JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {
            String sql = "delet from tbos where os=?";
            try {
                pst = conexao.prepareStatement(sql);
                pst.setString(1, txtOs.getText());
                int apagado = pst.executeUpdate();
                //CASO OS VALORES TENHAM SIDO APAGADO NO BD 
                if (apagado > 0) {
                    JOptionPane.showConfirmDialog(null, "OS excluida com sucesso");
                    //o metodo abaixo limpa os campos apos o cadastro de emissão
                    txtOs.setText(null);
                    txtData.setText(null);
                    txtCliId.setText(null);
                    txtOsEquip.setText(null);
                    txtOsDef.setText(null);
                    txtOsServ.setText(null);
                    txtOsTec.setText(null);
                    txtOsValor.setText(null);
                    //HABILITANDO OS OBJETOS
                    btnOsAdicionar.setEnabled(true);
                    txtCliPesquisar.setEnabled(true);
                    tblClientes.setVisible(true);
                }

            } catch (Exception e) {
                //caso ocorra uma exceção
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
    private void imprimir_os(){
    //gerado relatorio de serviço
        int confirma = JOptionPane.showConfirmDialog(null, "Confirma a impressão dessa OS?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {
            //EMITINDO O RELATORIO COM FRAMEWORK JASPER REPORT
            try {
                //USANDO A CLASS HashMap p criar um filtro
                HashMap filtro =new HashMap();
                filtro.put("os",Integer.parseInt(txtOs.getText()));
                //Usando a classe JasperPrint que esta na biblioteca
                JasperPrint print = JasperFillManager.fillReport("C:/reports/os.jasper", filtro, conexao);
                //A linha abaixo exibe o relatorio da classe JasperViewer
                JasperViewer.viewReport(print, false);
            } catch (NumberFormatException | JRException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
}

